package ArrangementTest;

public class ArrangementTest00 {

	public static void main(String[] args) {
		//자료형[] 변수 = {데이터 1, 데이터 2, 데이터 3, ...};
		//length : 배열의 크기
		//element : 데이터 값
		//index : 구분자
		String[] beer = {"최규","안승현","주형우"}; //beer라는 변수에 "최규","안승현","주형우"라는 데이터 넣기
		//인덱스 번호 :       0     1       2
		System.out.println(beer[0]); //beer 변수에서 "최규"라는 데이터 값을 출력
		System.out.println(beer[1]); //beer 변수에서 "안승현"이라는 데이터 값을 출력
		System.out.println(beer[2]); //beer 변수에서 "주형우"라는 데이터 값을 출력
		

	}

}
